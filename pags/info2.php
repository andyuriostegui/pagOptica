                <h2>Preguntas Frecuentes sobre Salud Ocular</h2>
                <div class="faq-item">
                    <h3>¿Con qué frecuencia debo hacerme un examen de la vista?</h3>
                    <p>Se recomienda hacerse un examen de la vista al menos una vez al año, o con mayor frecuencia si tiene condiciones de salud específicas que pueden afectar su visión.</p>
                </div>
                <div class="faq-item">
                    <h3>¿Qué es la degeneración macular y cómo puedo prevenirla?</h3>
                    <p>La degeneración macular es una enfermedad ocular que afecta la visión central. Para prevenirla, se recomienda llevar una dieta saludable, no fumar y usar protección solar para los ojos.</p>
                </div>
                <div class="faq-item">
                    <h3>¿Qué es el glaucoma y cómo se trata?</h3>
                    <p>El glaucoma es una enfermedad ocular que daña el nervio óptico. Puede tratarse con medicamentos, terapia láser o cirugía, dependiendo de la gravedad de la condición.</p>
                </div>
                <div class="faq-item">
                    <h3>¿Los dispositivos digitales afectan mi vista?</h3>
                    <p>El uso prolongado de dispositivos digitales puede causar fatiga ocular. Para mitigarlo, se recomienda seguir la regla 20-20-20: cada 20 minutos, mirar algo a 20 pies de distancia durante al menos 20 segundos.</p>
                </div>
                <div class="faq-item">
                    <h3>¿Qué son las cataratas y cómo se pueden tratar?</h3>
                    <p>Las cataratas son una opacidad del cristalino del ojo que afecta la visión. Pueden tratarse con cirugía para remover el cristalino opaco y reemplazarlo con un lente artificial.</p>
